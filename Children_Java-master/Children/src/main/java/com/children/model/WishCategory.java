	@NotEmpty
	@Column
	private String pictureUrl;

	@OneToMany(fetch = FetchType.EAGER, mappedBy = "category", cascade = CascadeType.ALL)
	private Set<Wish> wishes; 
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

